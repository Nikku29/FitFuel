-- 9. Storage Setup
-- Creating a bucket for user uploads (Set to non-public for production security)
INSERT INTO storage.buckets (id, name, public) VALUES ('user_uploads', 'user_uploads', false)
ON CONFLICT (id) DO NOTHING;

-- Storage policies
CREATE POLICY "Users can view own objects" ON storage.objects FOR SELECT USING (bucket_id = 'user_uploads' AND auth.uid() = owner);
CREATE POLICY "Users can insert to user_uploads" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'user_uploads' AND auth.uid() = owner);
CREATE POLICY "Users can update own objects" ON storage.objects FOR UPDATE USING (bucket_id = 'user_uploads' AND auth.uid() = owner);
CREATE POLICY "Users can delete own objects" ON storage.objects FOR DELETE USING (bucket_id = 'user_uploads' AND auth.uid() = owner);

-- 10. Automated Maintenance
-- Enable pg_cron for background cleanup (Requires 'pg_cron' extension enabled in Supabase Dashboard)
-- Note: On hosted Supabase, you might need to enable this in the UI under Extensions first.
CREATE EXTENSION IF NOT EXISTS pg_cron;

-- Schedule cleanup of anonymous sessions older than 24 hours (runs daily at 3:00 AM)
SELECT cron.schedule(
  'cleanup-anonymous-sessions',
  '0 3 * * *',
  $$ DELETE FROM public.anonymous_sessions WHERE created_at < NOW() - INTERVAL '24 hours' $$
);
