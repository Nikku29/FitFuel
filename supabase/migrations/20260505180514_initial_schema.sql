-- 1. Profiles Table
CREATE TABLE public.profiles (
  id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
  full_name TEXT,
  email TEXT,
  username TEXT,
  dob TEXT,
  gender TEXT,
  weight_kg NUMERIC,
  height_cm NUMERIC,
  location TEXT,
  fitness_goal TEXT,
  fitness_level TEXT,
  diet_preference TEXT,
  has_completed_onboarding BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can only access own profile" ON public.profiles FOR ALL USING (auth.uid() = id);

-- Trigger to create profile on signup
CREATE OR REPLACE FUNCTION public.handle_new_user() 
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name, created_at)
  VALUES (
    new.id, 
    new.email, 
    COALESCE(new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'name', 'User'), 
    NOW()
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

-- 2. Anonymous Sessions
CREATE TABLE public.anonymous_sessions (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  session_token TEXT UNIQUE,
  device_id TEXT,
  user_agent TEXT,
  ip_address TEXT,
  expires_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.anonymous_sessions ENABLE ROW LEVEL SECURITY;
-- Since they are anonymous, anyone can access by session token? Actually, typical apps don't strictly protect this table or use a token-based access.
-- Let's allow insert, and select/update if they know the token.
CREATE POLICY "Anon sessions select" ON public.anonymous_sessions FOR SELECT USING (true);
CREATE POLICY "Anon sessions insert" ON public.anonymous_sessions FOR INSERT WITH CHECK (true);
CREATE POLICY "Anon sessions update" ON public.anonymous_sessions FOR UPDATE USING (true);

